
# Print statement
print("Hello World!")
# Returns Hello World!

# Print statement with a new line
print("Hello World!\nHello World!\nHello World!")
# Returns   Hello World!
#           Hello World!
#           Hello World!
#           Hello World!

# Concatenate two strings using the '+'
print("Hello" + " " + "World")
# Returns Hello World

# Use input to capture user imput
input("What is your name?")

# Combining print and input statements
print("Hello " + input("What is your name? "))
# Returns 
# What is your name? Dave
# Hello Dave

# Assigning input to a variable and printing variable
name = input("What is your name? ")
print(name)

# Reassigning name variable
name = input("What is your name? ")
print(name)

name = "Joe"

# Get length of name and print
print(len(name))
# or
length = len(name)
print(length)

## Variables are named in snake_case
first_name = input("What is your first name? ")
last_name = input("What is your last name? ")
print("Hello " + first_name + " " + last_name + "!")






